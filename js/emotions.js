/**
 * 获取情绪的颜色
 * @param {string} emotion - 情绪词
 * @returns {string} - RGBA 颜色值
 */
export function getEmotionColor(emotion) {
    return EMOTION_COLORS[emotion] || 
           DEFAULT_COLORS[Math.floor(Math.random() * DEFAULT_COLORS.length)];
}

/**
 * 获取情绪所属的大类
 * @param {string} emotion - 情绪词
 * @returns {string} - 情绪大类名称
 */
export function getEmotionCategory(emotion) {
    for (const [category, children] of Object.entries(EMOTION_TREE)) {
        if (children.includes(emotion) || category === emotion) {
            return category;
        }
    }
    return 'default';
}

