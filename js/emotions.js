/**
 * 情绪随机器 - 提供更智能的随机算法
 */
class EmotionRandomizer {
    constructor() {
        this.recentEmotions = [];      // 最近出现的情绪
        this.recentCategories = [];    // 最近出现的类别
        this.maxRecentSize = 15;       // 追踪最近 N 个
        this.categoryBalance = {};     // 类别出现次数统计
    }

    /**
     * 重置状态（新游戏时调用）
     */
    reset() {
        this.recentEmotions = [];
        this.recentCategories = [];
        this.categoryBalance = {};
    }

    /**
     * 记录一个情绪的出现
     * @param {string} emotion - 情绪词
     */
    recordEmotion(emotion) {
        this.recentEmotions.push(emotion);
        if (this.recentEmotions.length > this.maxRecentSize) {
            this.recentEmotions.shift();
        }

        const category = getEmotionCategory(emotion);
        this.recentCategories.push(category);
        if (this.recentCategories.length > this.maxRecentSize) {
            this.recentCategories.shift();
        }

        this.categoryBalance[category] = (this.categoryBalance[category] || 0) + 1;
    }

    /**
     * 获取随机初始情绪组（8个，保证多样性）
     * @returns {string[]}
     */
    getRandomInitialEmotions() {
        const result = [];
        const usedCategories = new Set();
        const shuffled = [...INITIAL_EMOTION_POOL].sort(() => Math.random() - 0.5);

        for (const emotion of shuffled) {
            const category = getEmotionCategory(emotion);
            if (!usedCategories.has(category) && result.length < 8) {
                result.push(emotion);
                usedCategories.add(category);
            }
        }

        // 如果不够 8 个，从剩余中补充
        while (result.length < 8 && shuffled.length > 0) {
            const emotion = shuffled.pop();
            if (!result.includes(emotion)) {
                result.push(emotion);
            }
        }

        return result.sort(() => Math.random() - 0.5);
    }

    /**
     * 智能获取随机情绪（避免重复，平衡类别）
     * @param {string[]} currentEmotions - 当前屏幕上的情绪
     * @returns {string}
     */
    getSmartRandom(currentEmotions = []) {
        const allCandidates = this._getAllCandidates();
        
        // 过滤掉最近出现的和当前屏幕上的
        const excludeSet = new Set([
            ...this.recentEmotions.slice(-8),  // 最近 8 个
            ...currentEmotions
        ]);
        
        let candidates = allCandidates.filter(e => !excludeSet.has(e));

        // 如果过滤后太少，放宽限制
        if (candidates.length < 10) {
            candidates = allCandidates.filter(e => !currentEmotions.includes(e));
        }

        // 优先选择出现次数少的类别
        const underrepresentedCandidates = this._prioritizeUnderrepresented(candidates);
        
        if (underrepresentedCandidates.length > 0 && Math.random() > 0.3) {
            candidates = underrepresentedCandidates;
        }

        // 从候选中随机选择
        const chosen = candidates[Math.floor(Math.random() * candidates.length)] 
                       || allCandidates[Math.floor(Math.random() * allCandidates.length)];
        
        this.recordEmotion(chosen);
        return chosen;
    }

    /**
     * 获取所有候选情绪（大类 + 细分）
     * @private
     */
    _getAllCandidates() {
        const all = [...EMOTIONS];
        
        // 添加情绪树中的细分情绪
        for (const children of Object.values(EMOTION_TREE)) {
            for (const child of children) {
                if (!all.includes(child)) {
                    all.push(child);
                }
            }
        }
        
        return all;
    }

    /**
     * 优先选择出现次数少的类别中的情绪
     * @private
     */
    _prioritizeUnderrepresented(candidates) {
        const totalCount = Object.values(this.categoryBalance).reduce((a, b) => a + b, 0) || 1;
        const avgCount = totalCount / EMOTION_CATEGORIES.length;

        // 找出低于平均值的类别
        const underrepresented = EMOTION_CATEGORIES.filter(cat => 
            (this.categoryBalance[cat] || 0) < avgCount
        );

        if (underrepresented.length === 0) return candidates;

        // 从这些类别中筛选候选
        return candidates.filter(emotion => {
            const category = getEmotionCategory(emotion);
            return underrepresented.includes(category);
        });
    }

    /**
     * 获取某类别的随机细分情绪
     * @param {string} category - 情绪大类
     * @returns {string}
     */
    getRandomFromCategory(category) {
        const children = EMOTION_TREE[category];
        if (!children || children.length === 0) return category;
        
        // 过滤掉最近出现的
        const filtered = children.filter(e => !this.recentEmotions.slice(-5).includes(e));
        const candidates = filtered.length > 0 ? filtered : children;
        
        const chosen = candidates[Math.floor(Math.random() * candidates.length)];
        this.recordEmotion(chosen);
        return chosen;
    }
}

