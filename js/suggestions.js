/**
 * 根据情绪关键词获取建议类别
 * @param {string} emotion - 情绪词
 * @returns {string|null} - 建议类别
 */
function getSuggestionCategory(emotion) {
    for (const [category, keywords] of Object.entries(EMOTION_TO_SUGGESTION)) {
        for (const keyword of keywords) {
            if (emotion.includes(keyword)) {
                return category;
            }
        }
    }
    return null;
}

/**
 * 从指定类别随机获取一条建议
 * @param {string} category - 建议类别
 * @returns {string} - 建议文案
 */
function getRandomSuggestion(category) {
    const templates = SUGGESTION_TEMPLATES[category] || SUGGESTION_TEMPLATES.default;
    return templates[Math.floor(Math.random() * templates.length)];
}

/**
 * 根据用户戳破的情绪列表生成 AI 建议
 * @param {string[]} emotions - 情绪词数组（按频率排序）
 * @returns {string} - 生成的建议文本
 */
export function generateSuggestion(emotions) {
    const suggestions = [];
    const usedCategories = new Set();

    // 遍历用户戳破的情绪，匹配对应的建议类别
    for (const emotion of emotions) {
        const category = getSuggestionCategory(emotion);
        if (category && !usedCategories.has(category)) {
            suggestions.push(getRandomSuggestion(category));
            usedCategories.add(category);
        }
    }

    // 如果没有匹配到任何类别，使用默认建议
    if (suggestions.length === 0) {
        suggestions.push(getRandomSuggestion('default'));
    }

    // 限制建议数量，最多 4 条
    const finalSuggestions = suggestions.slice(0, 4);

    // 如果只有一条，加上通用的鼓励
    if (finalSuggestions.length === 1) {
        finalSuggestions.push(getRandomSuggestion('default'));
    }

    return finalSuggestions.join('\n\n');
}
